package com.vneuron.utils.dtos;

/**
 * {@link Name } class.
 *
 * @author kouali
 * @since 0.1.0
 */
public class Name {

	/** The name id. */
	public int name_id;

	/** The first name. */
	public String first_name;

	/** The last name. */
	public String last_name;

	/** The whole name. */
	public String whole_name;

	/** The original script name. */
	public String original_script_name;

	/** The name type. */
	public String name_type;

	/**
	 * Gets the name id.
	 *
	 * @return the name_id
	 */
	public int getName_id() {

		return name_id;
	}

	/**
	 * Sets the name id.
	 *
	 * @param name_id the name_id to set
	 */
	public void setName_id(int name_id) {

		this.name_id = name_id;
	}

	/**
	 * Gets the first name.
	 *
	 * @return the first_name
	 */
	public String getFirst_name() {

		return first_name;
	}

	/**
	 * Sets the first name.
	 *
	 * @param first_name the first_name to set
	 */
	public void setFirst_name(String first_name) {

		this.first_name = first_name;
	}

	/**
	 * Gets the last name.
	 *
	 * @return the last_name
	 */
	public String getLast_name() {

		return last_name;
	}

	/**
	 * Sets the last name.
	 *
	 * @param last_name the last_name to set
	 */
	public void setLast_name(String last_name) {

		this.last_name = last_name;
	}

	/**
	 * Gets the whole name.
	 *
	 * @return the whole_name
	 */
	public String getWhole_name() {

		return whole_name;
	}

	/**
	 * Sets the whole name.
	 *
	 * @param whole_name the whole_name to set
	 */
	public void setWhole_name(String whole_name) {

		this.whole_name = whole_name;
	}

	/**
	 * Gets the original script name.
	 *
	 * @return the original_script_name
	 */
	public String getOriginal_script_name() {

		return original_script_name;
	}

	/**
	 * Sets the original script name.
	 *
	 * @param original_script_name the original_script_name to set
	 */
	public void setOriginal_script_name(String original_script_name) {

		this.original_script_name = original_script_name;
	}

	/**
	 * Gets the name type.
	 *
	 * @return the name_type
	 */
	public String getName_type() {

		return name_type;
	}

	/**
	 * Sets the name type.
	 *
	 * @param name_type the name_type to set
	 */
	public void setName_type(String name_type) {

		this.name_type = name_type;
	}

}
